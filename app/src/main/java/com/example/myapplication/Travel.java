package com.example.myapplication;

public class Travel {

    int images;
    String name;

    public Travel(int images, String name) {
        this.images = images;
        this.name = name;
    }

    public int getImages() {
        return images;
    }

    public void setImages(int images) {
        this.images = images;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
